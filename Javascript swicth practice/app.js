var day = prompt("\"Enter here a days name .\"");
switch (day){
    case "sunday":
    alert("Today is holiday");
    break;
    case "monday":
    alert("Today is a work day");
    break;
    case "tuesday":
    alert("today is a also work day");
    break;
    case "wednesday":
    alert("Today will be the sport day in school");
    break;
    case "thursday":
    alert("Today is a writing day in school")        
    break;
    case "friday":
    alert("Today is half day in school");
    break;
    case "saturday":
    alert("today is a speaking class in school");
    break;   
    default:{
        alert("something is wrong")
    }      
}

